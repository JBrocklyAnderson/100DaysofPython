year = int(input("If you don't mind me asking, what year were you born? "))
if year <= 1882:
  print("Welcome to the world of tomorrow, yee Old Wise One 🚀")
elif year >= 1883 and year <= 1900:
  print("You've finally been found as a member of the Lost Generation 😶‍🌫️")
elif year >= 1901 and year <= 1927:
  print("What a great thing to be born of the Greatest Generation 🙌")
elif year >= 1928 and year <= 1945:
  print("Shhhhh... You were born in the Silent Generation 😴")
elif year >= 1946 and year <= 1964:
  print("You hit the world like an explosion, Baby Boomer 💥")
elif year >= 1965 and year <= 1980:
  print("Generation X marks the spot you were born in 🗺️")
elif year >= 1981 and year <= 1996:
  print("✈️☄️ Y2K came and went, but you will always be a Millenial")
elif year >= 1997 and year <= 2012:
  print("Zoom in to the Zoomers, zoom out to Future 🔍")
elif year >= 2013 and year <= 2025:
  print("You're just starting to grow up, litle Alpha 👩‍🍼")
else:
  print("I've come face to face with Father Time Travel 🕓")